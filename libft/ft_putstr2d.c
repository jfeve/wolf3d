#include "libft.h"

void	ft_putstr2d(char **tab)
{
	int i;
	int k;

	i = 0;
	k = 0;
	write(1, "/", 1);
	while (tab[k])
	{
		write(1, " ", 1);
		while (tab[k][i])
		{
			write(1, &tab[k][i], 1);
			i++;
		}
		k++;
		i = 0;
	}
	write(1, "/", 1);
}
