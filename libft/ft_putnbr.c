#include "libft.h"

void	ft_putnbr(int n)
{
	long lg;
	char a;

	lg = n;
	a = '0';
	if (lg < 0)
	{
		write(1, "-", 1);
		lg = -lg;
	}
	if (lg >= 0 && lg <= 9)
	{
		a = a + lg;
		write(1, &a, 1);
	}
	else
	{
		ft_putnbr(lg / 10);
		ft_putnbr(lg % 10);
	}
}
